package LOOPS;

public class EVEN_NO 
{
public static void main(String[]args)
{
	for(int a =1 ;a<=100; a++)
	{System.out.println(a);        //1_100 numbers
		}
	for(int J=2; J<=100; J=J+2){
		System.out.println(J);//2 4 6 8.....98 100 even no
	}
	for(int x=1;x<=100; x=x+2) {
		System.out.println(x); //1 3 5 7.....97 99 odd no
	}
	for(int z=100; z>=1;z--);
	
}}